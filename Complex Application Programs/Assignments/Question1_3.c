// Problem Statement : Program to print 5 to 1 numbers on screen
//
//////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Input :         Nothing
//  Output :        Returns Nothing
//  Description :   Prints 5 to 1 using for loop
//  Author :        Dinesh Devidasrao Kadam
//  Date :          24 April 2023
//
////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;
    i = 5;
    for(i=5; i>=1; i--)
    {
        printf("%d\n", i);
    }
}

/////////////////////////////////////////////////////////////
// Entry Point Function
////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}